(deftest label-locations-test
  (testing "Can find the maze coordinates of all of the labels"
    (is (= {[0 7] ["AA" :outer],
            [6 0] ["BC" :outer], [11 0] ["DE" :outer], [13 0] ["FG" :outer],
            [4 7] ["BC" :inner],
            [8 4] ["DE" :inner],
            [10 9] ["FG" :inner],
            [14 11] ["ZZ" :outer]}
           (t/label-locations d20-s00)))
    (is (= {[0 17] ["AA" :outer]
            [13 0] ["DI" :outer], [17 0] ["JO" :outer], [21 0] ["YN" :outer],
            [32 9] ["BU" :outer], [32 13] ["JP" :outer], [32 17] ["CP" :outer],
            [9 30] ["VT" :outer], [15 30] ["AS" :outer], [19 30] ["LF" :outer], [21 30] ["QG" :outer],
            [6 15] ["AS" :inner], [6 19] ["CP" :inner],
            [19 6] ["DI" :inner]
            [26 11] ["JO" :inner], [26 13] ["LF" :inner], [26 19] ["JP" :inner],
            [11 24] ["YN" :inner], [15 24] ["QG" :inner], [19 24] ["BU" :inner], [21 24] ["VT" :inner],
            [15 0] ["ZZ" :outer]}
           (t/label-locations d20-s01)))))


(deftest neighbor-coords-test
  (testing "Neighbor lookup follows portals"
    (let [graph (:graph (t/maze-with-portals (t/load-maze d20-s00)))]
      (is (= [[1 7] [6 0]] (edges graph [4 7])))
      (is (= [[6 0] [11 0]] (edges graph [8 4])))
      (is (= [[11 0] [10 9]] (edges graph [13 0]))))))

(deftest shortest-path-test
  (testing "Can find the shortest path when using portals"
    (is (= 23 (t/solve-maze d20-s00)))
    (is (= 58 (t/solve-maze d20-s01)))))

